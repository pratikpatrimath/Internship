    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String BookID=jTextField1.getText();
        String studentID=jTextField2.getText();
        
        try
        {
            Connection con=ConnectionProvider.getCon();
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select *from issue where bookID='"+BookID+"' and studentID='"+studentID+"'");
            if(rs.next())
            {
                jTextField3.setText(rs.getString(3));
                jTextField4.setText(rs.getString(4));
                jTextField2.setEditable(false);
                jTextField1.setEditable(false);
                
            }
            else
            {
                JOptionPane.showConfirmDialog(null,"Book is not issued to this student ");
                setVisible(false);
                new returnBook1().setVisible(true);
            }
        }
        catch(Exception e)
        {
            JOptionPane.showConfirmDialog(null,"Connection error ");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

